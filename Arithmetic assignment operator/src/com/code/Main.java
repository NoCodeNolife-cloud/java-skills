package com.code;

public class Main {

    public static void main(String[] args) {
        int a=1;
        int b=2;
        a+=b;
        System.out.println(a);

        a+=b+3;
        System.out.println(a);
        a-=b;
        System.out.println(a);

        a*=b;
        System.out.println(a);

        a/=b;
        System.out.println(a);

        a%=b;
        System.out.println(a);
    }
}
