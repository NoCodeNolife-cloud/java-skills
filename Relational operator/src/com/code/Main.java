package com.code;

public class Main {

    public static void main(String[] args) {
	    int value1=1;
	    int value2=2;

	    // check for the relationship of the value1 and value2
	    if(value1==value2){
	        System.out.println("value1 == value2");
        }
	    if(value1 != value2){
            System.out.println("value1 != value2");
        }
	    if(value1 > value2){
            System.out.println("value1 > value2");
        }
	    if(value1<value2){
            System.out.println("value1 < value2");
        }
	    if(value1<=value2){
            System.out.println("value1 <= value2");
        }
	    if(value1>=value2){
	        System.out.println("value1 >= value2");
        }
    }
}
