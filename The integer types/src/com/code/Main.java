package com.code;

public class Main {

    public static void main(String[] args) {
        System.out.println("默认整数常量     =  "+16);
        byte a=16;
        short b=16;
        int c=16;
        long d=16L;
        long e=16l;

        System.out.println("byte整数       =  "+a);
        System.out.println("short整数      =  "+b);
        System.out.println("int整数        =  "+c);
        System.out.println("long整数       =  "+d);
        System.out.println("long整数       =  "+e);
    }
}
